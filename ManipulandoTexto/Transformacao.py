frase = "Curso em Video Python"

#Substituição
print(frase.replace('Python', 'Java'))

#Método .upper() = coloca tudo em maiúsculo
print(frase.upper())

#Método .lower() = coloca tudo em minúsculo
print(frase.lower())

#Método .capitalize() = coloca as primeiras letras em maiúsculo menos preposição
print(frase.capitalize())

#Método .tittle  = coloca as primeiras letras em maiúsculo até mesmo preposição
print(frase.title())

frase2 = "Aprenda Python"

#Para remover os espaços iniciais e finais de uma string - .strip()
print(frase.strip())

#Para remover os últimos espaços à direita - .strip()
print(frase.rstrip())

#Para remover os últimos espaços à esquerda - .strip()
print(frase.lstrip())
