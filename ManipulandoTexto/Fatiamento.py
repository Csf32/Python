frase = "Curso em video Python"
print(frase[9])

# Imprimir de um intervalo - O último número não entra, neste caso é o 13 = o
print(frase[9:13])

# Não é muito utilizado colocar um a mais para pegar o último, neste caso o 20 = n
print(frase[9:21])

# Fatiamento em grupo pulando :x
print(frase[9:21:2])

# Começar de 0 = :x
print(frase[:5])

# Começar e ir até o final
print(frase[15:])

# Nada no meio, então vai até o final e saltando
print(frase[9::3])
